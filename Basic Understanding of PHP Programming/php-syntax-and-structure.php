<!-- Embedding PHP in HTML:
You can mix PHP code within your HTML code by enclosing it within the tags.
For example, to display the current date in an HTML paragraph, you can do this: -->

<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta http-equiv="X-UA-Compatible" content="IE=edge">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>PHP Syntax and Structure</title>
</head>
<body>
  <p>
    Today is <?php echo date("Y-m-d");?>
  </p>
</body>
</html>

